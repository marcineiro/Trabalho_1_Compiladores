    public void contarPalavras(){
        char c;
        int i = 0;
        while(i<entrada.length()){
            c = entrada.charAt(i);
            while(c==' '&&i<entrada.length()){
                i++;
                c = entrada.charAt(i);
            }
            while(c!=' '&&c!='\n'){
                i++;
                if(i<entrada.length()){
                    c = entrada.charAt(i);
                } else {
                    c = '\n';
                }
                
            }
            i++;
            numPalavras++;
        }
    }
